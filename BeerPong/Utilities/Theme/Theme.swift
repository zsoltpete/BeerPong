import Foundation
import UIKit

extension UIColor {
    
    class var lightBeer: UIColor {
        return UIColor(red: 217.0 / 255.0, green: 130.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
    class var darkBeer: UIColor {
        return UIColor(red: 34.0 / 255.0, green: 0.0 / 255.0, blue: 0.0 / 255.0, alpha: 1.0)
    }
    
}
